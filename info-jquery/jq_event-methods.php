					<h2>Event Handler Attachment</h2>
					<div class="shadow">
						<table class="data">
							<tr>
								<th style="width: 123px;">Method</th>
								<th>Description</th>
							</tr>						
							<tr>
								<td><code>bind()</code></td>
								<td>Bind an event handler to be fired when the given type of event is sent to the element. In general, use the <code>on()</code> method instead.</td>
							</tr>												
							<tr>
								<td><code>delegate()</code></td>
								<td>Bind one or more event handlers to be fired when the given type of event is/are sent to a descendant element matching selector.</td>
							</tr>																							
							<tr>
								<td><code>jQuery.proxy()</code></td>
								<td>Takes an existing function and returns a new one that will always have a particular context.</td>
							</tr>
							<tr>
								<td><code>on()</code></td>
								<td>Attaches event handlers to the selected elements.</td>
							</tr>
							<tr>
								<td><code>off()</code></td>
								<td>Removes event handlers that were with the <code>on()</code> method.</td>
							</tr>
							<tr>
								<td><code>one()</code></td>
								<td>Attaches one or more event handlers to selected elements. The handler is executed at most once per element.</td>
							</tr>
							<tr>
								<td><code>trigger()</code></td>
								<td>Execute all handlers and behaviors attached to the selected elements for the given event type.</td>
							</tr>
							<tr>
								<td><code>triggerHandler()</code></td>
								<td>Execute all handlers attached to the selected elements for an event.</td>
							</tr>
							<tr>
								<td><code>unbind()</code></td>
								<td>Remove a previously-attached event handler from the elements.</td>
							</tr>
							<tr>
								<td><code>undelegate()</code></td>
								<td>Removes the event handlers bindings on the element that have been previously bound with <code>delegate()</code> method.</td>
							</tr>						
						</table>
					</div>
