                <p class="topic">Topic: <a href="htl_tags.php">HTML5 Tags Reference</a><span class="topic-nav"><a href="htl_canvas-tag.php"><i class="fa fa-arrow-left" aria-hidden="true"></i></a><a href="ht_center-tag.php"><i class="fa fa-arrow-right" aria-hidden="true"></i></a></span></p>
                <h2>Description</h2>	
                <p>The <code>&lt;caption&gt;</code> tag defines the title of a table. The caption text should describe the nature of the table. The <code>caption</code> tag only permitted immediately after the <code><a href="ht_table-tag.php">&lt;table&gt;</a></code> start tag. A <code>&lt;table&gt;</code> element may only contain one <code>&lt;caption&gt;</code> element.</p>
                <p>The following table summarizes the usages context and the version history of this tag.</p>
