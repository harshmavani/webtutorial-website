                        </tr>
                        <tr>
                            <th>Version:</th>
                            <td>HTML 2, 3.2, 4, 4.01, 5; XHTML 1.0, 1.1</td>
                        </tr>
                    </table>
                </div>
                <div class="usage">
                    <h2><em>Difference between <code>&lt;strong&gt;</code> and <code>&lt;b&gt;</code> tag</em></h2>
                    <ul>
                        <li><p>Both tags renders the text it encloses in a bold typeface, but the <code>&lt;strong&gt;</code> element which represents text of certain importance, the <code><a href="ht_b-tag.php">&lt;b&gt;</a></code> tag doesn't convey any semantic information about the contained text.</p></li>
                        <li><p>It is strongly recommended to avoid the use of <code>&lt;b&gt;</code> tag; using CSS property <code><a href="../info-css/css-font-weight-property.php">font-weight</a></code> with <code>bold</code> value would be a better choice for making the text bold.</p></li>
                    </ul>
                </div>
