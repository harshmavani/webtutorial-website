                <h1>HTML <code>&lt;strong&gt;</code> Tag</h1>
                <p class="topic">Topic: <a href="htl_tags.php">HTML5 Tags Reference</a><span class="topic-nav"><a href="ht_strike-tag.php"><i class="fa fa-arrow-left" aria-hidden="true"></i></a><a href="ht_style-tag.php"><i class="fa fa-arrow-right" aria-hidden="true"></i></a></span></p>
                <h2>Description</h2>
                <p>The <code>&lt;strong&gt;</code> tag specifies enclosed text as strongly emphasized text.</p>
                <p>The following table summarizes the usages context and the version history of this tag.</p>
                <div class="shadow">
                    <table class="data description">
                        <tr>
