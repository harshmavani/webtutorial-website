                <h2>Description</h2>
                <p>The <em>heading tags</em> are used to define HTML headings. There are six levels of headings, from <code>&lt;h1&gt;</code> (most important) to <code>&lt;h6&gt;</code> (least important). These tags are logical block tags that's not only improves the accessibility but also aids in search engine optimization &mdash; information in higher heading levels is given more weight.</p>
                <p>The following table summarizes the usages context and the version history of <em>heading tags</em>.</p>
