                <h2>Description</h2>
                <p>The <code>&lt;wbr&gt;</code> (stands for <em>Word Break Opportunity</em>) element specifies a position within text where the browser may optionally break a line if necessary.</p>
                <p>The following table summarizes the usages context and the version history of this tag.</p>
