                <h2>HTML Tags Vs Elements</h2>
                <p>Technically, an HTML element is the collection of start tag, its attributes, an end tag and everything in between. On the other hand an HTML tag (either opening or closing) is used to mark the start or end of an element, as you can see in the above illustration.</p>
                <p>However, in common usage the terms HTML element and HTML tag are interchangeable i.e. a tag is an element is a tag. For simplicity's sake of this website, the terms "tag" and "element" are used to mean the same thing &mdash; as it will define something on your web page.</p>
                <hr />
                <h2>Case Insensitivity in HTML Tags and Attributes</h2>
                <p>In HTML, tag and attribute names are not case-sensitive (but most attribute values are case-sensitive). It means the tag <code>&lt;P&gt;</code>, and the tag <code>&lt;p&gt;</code> defines the same thing in HTML which is a paragraph.</p>
                <p>But in <a href="../xdes-html.php">XHTML</a> they are case-sensitive and the tag <code>&lt;P&gt;</code> is different from the tag <code>&lt;p&gt;</code>.</p>
