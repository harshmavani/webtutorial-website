                <h2 id="empty-elements">Empty HTML Elements</h2>
                <p>Empty elements (also called self-closing or void elements) are not container tags &mdash; that means, you can not write <code>&lt;hr&gt;<i>some content</i>&lt;/hr&gt;</code> or <code>&lt;br&gt;<i>some content</i>&lt;/br&gt;</code>.</p>
                <p>A typical example of an empty element, is the <code>&lt;br&gt;</code> element, which represents a line break. Some other common empty elements are <code>&lt;img&gt;</code>, <code>&lt;input&gt;</code>, <code>&lt;link&gt;</code>, <code>&lt;meta&gt;</code>, <code>&lt;hr&gt;</code>, etc.</p>
