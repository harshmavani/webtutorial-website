                <h2>HTML Elements Types</h2>
                <p>Elements can be placed in two distinct groups: <em><a href="../des-css/css-visual-formatting.php#block-level">block level</a></em> and <em><a href="../des-css/css-visual-formatting.php#inline-level">inline level</a></em> elements. The former make up the document's structure, while the latter dress up the contents of a block.</p>
                <p>Also, a block element occupies 100% of the available width and it is rendered with a line break before and after. Whereas, an inline element will take up only as much space as it needs.</p>
                <p>The most commonly used block-level elements are <code>&lt;div&gt;</code>, <code>&lt;p&gt;</code>, <code>&lt;h1&gt;</code> through <code>&lt;h6&gt;</code>, <code>&lt;form&gt;</code>, <code>&lt;ol&gt;</code>, <code>&lt;ul&gt;</code>, <code>&lt;li&gt;</code>, and so on. Whereas, the commonly used inline-level elements are <code>&lt;img&gt;</code>, <code>&lt;a&gt;</code>, <code>&lt;span&gt;</code>, <code>&lt;strong&gt;</code>, <code>&lt;b&gt;</code>, <code>&lt;em&gt;</code>, <code>&lt;i&gt;</code>, <code>&lt;code&gt;</code>, <code>&lt;input&gt;</code>, <code>&lt;button&gt;</code>, etc.</p>
                <p>You will learn about these elements in detail in upcoming chapters.</p>
