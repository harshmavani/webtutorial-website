                <h1>HTML <span>URL</span></h1>
                <p class="summary">In this tutorial you will learn about the different parts of a URL in detail.</p>
                <h2>What is URL?</h2>
                <p>URL stands for <b>U</b>niform <b>R</b>esource <b>L</b>ocator is the global address of documents and other resources on the World Wide Web. Its main purpose is to identify the location of a document and other resources available on the internet, and specify the mechanism for accessing it through a web browser.</p>
                <p>For instance, if you look at the address bar of your browser you will see:</p>
                <div class="green-box break">https://www.tutorialrepublic.com/des-html/ht_url.php</div>
                <p>&mdash; This is the URL of the web page you are viewing right now.</p>
                <hr />
                <h2>The URL Syntax</h2>
                <p>The general syntax of URLs is the following:</p>
                <div class="sky-box break">scheme://host:port/path?query-string#fragment-id</div>
                <p>A URL has a linear structure and normally consists of some of the following:</p>
                <ul>
                    <li><strong>Scheme name</strong> &mdash; The scheme identifies the protocol to be used to access the resource on the Internet. The scheme names followed by the three characters <code>://</code> (a colon and two slashes). The most commonly used protocols are <code>http://</code>, <code>https://</code>, <code>ftp://</code>, and <code>mailto://</code>.</li>
                    <li class="alt"><strong>Host name</strong> &mdash; The host name identifies the host where resource is located. A hostname is a domain name assigned to a host computer. This is usually a combination of the host's local name with its parent domain's name. For example, <code>www.tutorialrepublic.com</code> consists of host's machine name <code>www</code> and the domain name <code>tutorialrepublic.com</code>.</li>
                    <li><strong>Port Number</strong> &mdash; Servers often deliver more than one type of service, so you must also tell the server what service is being requested. These requests are made by port number. Well-known port numbers for a service are normally omitted from the URL. For example, web service HTTP runs by default over port 80, HTTPS runs by default over port 443.</li>
                    <li class="alt"><strong>Path</strong> &mdash; The path identifies the specific resource within the host that the user wants to access. For example, <code>/html/ht_url.php</code>, <code>/news/technology/</code>, etc.</li>
                    <li><strong>Query String</strong> &mdash; The query string contains data to be passed to server-side scripts, running on the web server. For example, parameters for a search. The query string preceded by a question mark (<code>?</code>), is usually a string of name and value pairs separated by ampersand (<code>&amp;</code>), for example, <code>?first_name=John&amp;last_name=Corner</code>, <code>q=mobile+phone</code>, and so on.</li>
                    <li class="alt"><strong>Fragment identifier</strong> &mdash; The fragment identifier, if present, specifies a location within the page. Browser may scroll to display that part of the page. The fragment identifier introduced by a hash character (<code>#</code>) is the optional last part of a URL for a document.</li>
                </ul>
