                <h1>HTML <span>Tutorial</span></h1>
                <p class="summary">HTML is the main markup language for describing the structure of web pages.</p>
                <div class="intro-image">
                    <img src="../assets/images/html5.jpg" alt="HTML Illustration" />
                </div>
                <p>HTML stands for HyperText Markup Language. HTML is the basic building block of World Wide Web.</p>
				<p>Hypertext is text displayed on a computer or other electronic device with references to other text that the user can immediately access, usually by a mouse click or key press.</p>
                <p>Apart from text, hypertext may contain tables, lists, forms, images, and other presentational elements. It is an easy-to-use and flexible format to share information over the Internet.</p>
                <p>Markup languages use sets of markup tags to characterize text elements within a document, which gives instructions to the web browsers on how the document should appear.</p>
                <p>HTML was originally developed by Tim Berners-Lee in 1990. He is also known as the father of the web. In 1996, the World Wide Web Consortium (W3C) became the authority to maintain the HTML specifications. HTML also became an international standard (<span title="International Organization for Standardization">ISO</span>) in 2000. HTML5 is the latest version of HTML. HTML5 provides a faster and more robust approach to web development.</p>
