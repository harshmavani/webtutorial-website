                <p class="summary">In this tutorial you will learn what the document type declaration is in HTML.</p>
                <h2>Understanding the HTML5 Doctype</h2>
                <p>A Document Type Declaration, or DOCTYPE for short, is an instruction to the web browser about the version of markup language in which a web page is written.</p>
                <p>A DOCTYPE declaration appears at the top of a web page before all other elements. According to the HTML specification or standards, every HTML document requires a valid document type declaration to insure that your web pages are displayed the way they are intended to be displayed.</p>
                <p>The doctype declaration is usually the very first thing defined in an HTML document (even before the opening <a href="../info-html/ht_ht_tag.php"><code>&lt;html&gt;</code></a> tag); however the doctype declaration itself is not an HTML tag.</p>
                <p>The DOCTYPE for HTML5 is very short, concise, and case-insensitive.</p>
                <div class="sky-box break">&lt;!DOCTYPE html&gt;</div>
                <p>Doctypes for earlier versions of HTML were longer because the HTML language was SGML-based and therefore required a reference to a DTD, but they are <a href="../definitions.php#obsolete">obsolete</a> now.</p>
                <p>With HTML5 this is no longer the case and the doctype declaration is only needed to enable the standard mode for documents written using the HTML syntax.</p>
