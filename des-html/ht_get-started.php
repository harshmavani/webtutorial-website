				<h2>HTML Tags and Elements</h2>
                <p>HTML is written in the form of HTML elements consisting of markup tags. These markup tags are the fundamental characteristic of HTML. Every markup tag is composed of a keyword, surrounded by angle brackets, such as <code>&lt;html&gt;</code>, <code>&lt;head&gt;</code>, <code>&lt;body&gt;</code>, <code>&lt;title&gt;</code>, <code>&lt;p&gt;</code>, and so on.</p>
                <p>HTML tags normally come in pairs like <code>&lt;html&gt;</code> and <code>&lt;/html&gt;</code>. The first tag in a pair is often called the opening tag (or start tag), and the second tag is called the closing tag (or end tag).</p>
                <p>An opening tag and a closing tag are identical, except a slash (<code>/</code>) after the opening angle bracket of the closing tag, to tell the browser that the command has been completed.</p>
                <p>In between the start and end tags you can place appropriate contents. For example, a paragraph, which is represented by the <code>p</code> element, would be written as:</p>
