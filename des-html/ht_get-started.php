                <h2>Explanation of code</h2>
                <p>You might think what that code was all about. Well, let's find out.</p>
                <ul>
                    <li>The first line <code>&lt;!DOCTYPE html&gt;</code> is the <a href="ht_doctypes.php">document type declaration</a>. It instructs the web browser that this document is an HTML5 document. It is case-insensitive.</li>
                    <li>The <code>&lt;head&gt;</code> element is a container for the tags that provides information about the document, for example, <code>&lt;title&gt;</code> tag defines the title of the document.</li>
                    <li>The <code>&lt;body&gt;</code> element contains the document's actual content (paragraphs, links, images, tables, and so on) that is rendered in the web browser and displayed to the user.</li>
                </ul>
                <p>You will learn about the different HTML elements in detail in the upcoming chapters. For now, just focus on the basic structure of the HTML document.</p> 
