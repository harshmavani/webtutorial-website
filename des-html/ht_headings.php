                <h2>Organizing Content with Headings</h2>
                <p>Headings help in defining the hierarchy and the structure of the web page content.</p>
                <p>HTML offers six levels of heading tags, <code>&lt;h1&gt;</code> through <code>&lt;h6&gt;</code>; the higher the heading level number, the greater its importance &mdash; therefore <code>&lt;h1&gt;</code> tag defines the most important heading, whereas the <code>&lt;h6&gt;</code> tag defines the least important heading in the document.</p>
                <p>By default, browsers display headings in larger and bolder font than normal text. Also, <code>&lt;h1&gt;</code> headings are displayed in largest font, whereas <code>&lt;h6&gt;</code> headings are displayed in smallest font.</p>
