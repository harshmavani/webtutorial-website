                <h1>HTML <span>Validation</span></h1>
                <p class="summary">In this tutorial you will learn how to validate your HTML code to make sure that your HTML code follows the current web standards and is free from errors.</p>
                <h2>Why Validate Your HTML Code</h2>
                <p>As a beginner it is very common that you will make mistake in writing your HTML code. Incorrect or non-standard code may cause unexpected results in how your page displayed or function in browsers.</p>
                <p>To prevent this you can test or validate your HTML code against the formal guidelines and standards set by the Wide Web Consortium (W3C) for HTML/XHTML web pages.</p>
                <p class="space">The World Wide Web Consortium provide a simple online tool (<a href="https://validator.w3.org/" target="_blank" rel="nofollow">https://validator.w3.org/</a>) that automatically check your HTML code and point out any problems/errors your code might have, such as missing closing tags or missing quotes around attributes.</p>
                <h2>Validating a Web <i class="fa fa-arrow-right" aria-hidden="true"></i>
 Page</h2>
                <p>Validating a web page is a process of ensuring that it conforms to the norms or standards defined by the World Wide Web Consortium (W3C) which is the authority to maintain HTML standards.</p>
                <p>There are several specific reasons for validating a web page, some of them are:</p>
                <ul>
                    <li>It helps to create web pages that are cross-browser, cross-platform compatible. It also likely to be compatible with the future version of web browsers and web standards.</li>
                    <li>Standards compliant web pages increase the search engine spiders and crawlers visibility, as a result your web pages will more likely be appear in search results.</li>
                    <li>It will reduce unexpected errors and make your web pages more accessible to the visitor.</li>
                </ul>
