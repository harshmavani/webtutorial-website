                <h1>HTML <span>Entities</span></h1>
                <p class="summary">In this tutorial you will learn how to use HTML entities to display special characters and symbols.</p>
                <h2>What is HTML Entity?</h2>
                <p>Some characters are reserved in HTML, e.g. you cannot use the less than (<code>&lt;</code>) or greater than (<code>&gt;</code>) signs or angle brackets within your text, because the browser could mistake them for markup, while some characters are not present on the keyboard like copyright symbol &copy;.</p>
                <p class="space">To display these special characters, they must be replaced with the character entities. Character entity references, or entities for short, enable you to use the characters that cannot be expressed in the document's character encoding or that cannot be entered by a keyboard.</p>
