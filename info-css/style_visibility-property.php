                <h2>Property Values</h2>
                <p>The following table describes the values of this property.</p>
                <div class="shadow space">
                    <table class="data">
                        <tr>
                            <th style="width:85px;">Value</th>
                            <th>Description</th>
                        </tr>
                        <tr>
                            <td><code>visible</code></td>
                            <td>The box and its contents are visible. This is default.</td>
                        </tr>
                        <tr>
                            <td><code>hidden</code></td>
                            <td>The box and its content are invisible, but still affect the layout of the page.</td>
                        </tr>
                        <tr>
                            <td><code>collapse</code></td>
                            <td>
                                <p class="up">Only for certain internal table objects: rows, row groups, columns and column groups. It removes the objects, but it does not affect the table layout in any other way. The space occupied by the table object will be filled by subsequent siblings.</p>
                                <p>If collapse is specified for other element, it causes the same behavior as hidden.</p>
                            </td>
                        </tr>
						<tr>
							<td><code>initial</code></td>
							<td>Sets this property to its default value.</td>
						</tr>
                        <tr>
                            <td><code>inherit</code></td>
                            <td>If specified, the associated element takes the <a href="../definitions.php#computed-value">computed value</a> of its parent element <code>visibility</code> property.</td>
                        </tr>
                    </table>
