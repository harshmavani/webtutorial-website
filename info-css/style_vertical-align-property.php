                <h1>CSS <code>vertical-align</code> Property</h1>
                <p class="topic">Topic: <a href="stylel_properties.php">CSS3 Properties Reference</a><span class="topic-nav"><a href="style_unicode-bidi-property.php"><i class="fa fa-arrow-left" aria-hidden="true"></i></a><a href="style_visibility-property.php"><i class="fa fa-arrow-right" aria-hidden="true"></i></a></span></p>	
                <h2>Description</h2>
                <p>This <code>vertical-align</code> CSS property controls the vertical alignment of boxes generated by an <a href="../des-css/style_visual-formatting.php#inline-level">inline-level</a> element (usually text and images) with respect to the text baseline within a <a href="../des-css/style_visual-formatting.php#block-level">block-level</a> box, or of table cells within a row.</p>
                <p>The following table summarizes the usages context and the version history of this property.</p>
                <div class="shadow">
                    <table class="data description">
                        <tr>
                            <th>Default value:</th>
                            <td><code>baseline</code></td>
                        </tr>
                        <tr>
                            <th>Applies to:</th>
                            <td>Inline-level and table-cell elements</td>
                        </tr>
                        <tr>
                            <th>Inherited:</th>
                            <td>No</td>
                        </tr>
						<tr>
							<th>Animatable:</th>
							<td>Yes. <a href="style_animatable-properties.php"><i>See</i> <em>animatable properties</em></a>.</td>
						</tr>
                        <tr>
                            <th>Version:</th>
                            <td>CSS 1, 2, 3</td>
                        </tr>
                    </table>
                </div>	
                <hr />	
