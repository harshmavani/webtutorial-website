                <h2>Property Values</h2>
                <p>The following table describes the values of this property.</p>
                <div class="shadow space">
                    <table class="data">
                        <tr>
                            <th style="width:85px;">Value</th>
                            <th>Description</th>
                        </tr>
                        <tr>
                            <td><code>baseline</code></td>
                            <td>Align the baseline of the element's box with the baseline of the parent box.</td>
                        </tr>
                        <tr>
                            <td><code>sub</code></td>
                            <td>Lower the baseline of the box to the position suitable for subscripts of the parent's box.</td>
                        </tr>
                        <tr>
                            <td><code>super</code></td>
                            <td>Raises the baseline of the box to the position suitable for superscripts of the parent's box.</td>
                        </tr>
                        <tr>
                            <td><code>top</code></td>
                            <td>Align the top of the element and its descendants with the top of the line box.</td>
                        </tr>
						<tr>
                            <td><code>text-top</code></td>
                            <td>Align the top of the element with the top of the parent element's font.</td>
                        </tr>
                        <tr>
                            <td><code>middle</code></td>
                            <td>Align the middle of the element with the point that's half the parent's <a href="../des-css/style_units.php">x-height</a> above the baseline of the parent box.</td>
                        </tr>
                        <tr>
                            <td><code>bottom</code></td>
                            <td>Align the bottom of the element and its descendants with the bottom of the line box.</td>
                        </tr>                        
                        <tr>
                            <td><code>text-bottom</code></td>
                            <td>Align the bottom of the element with the bottom of the parent element's font.</td>
                        </tr>
						<tr>
                            <td><i>length</i></td>
                            <td>A length value in <code>px</code>, <code>pt</code>, <code>cm</code>, <code>em</code>, etc. Length values raise (positive value) or lower (negative value) the box by this distance from the baseline of its parent. The value 0px is same as <code>baseline</code>.</td>
                        </tr>
                        <tr>
                            <td><i>percentage</i></td>
                            <td>Raise (positive value) or lower (negative value) the box by this distance (a percentage of the <code><a href="style_line-height-property.php">line-height</a></code> value). The value 0% is same as <code>baseline</code>.</td>
                        </tr>                        
						<tr>
							<td><code>initial</code></td>
							<td>Sets this property to its default value.</td>
						</tr>
                        <tr>
                            <td><code>inherit</code></td>
                            <td>If specified, the associated element takes the <a href="../definitions.php#computed-value">computed value</a> of its parent element <code>vertical-align</code> property.</td>
                        </tr>
                    </table>
                </div>
