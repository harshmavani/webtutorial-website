                <h1>Bootstrap <span>Buttons</span></h1>
                <p class="summary">In this tutorial you will learn how to create and modify buttons with Bootstrap.</p>
                <h2>Creating Buttons with Bootstrap</h2>
                <p class="space">Buttons are the integral part of a website and application. They are used for various purposes like, submit or reset an <a href="../des-html/ht_forms.php">HTML form</a>, performing interactive actions such as showing or hiding something on a web page on click of the button, redirecting user to another page, and so on. Bootstrap provides a quick and easy way to create and customize the buttons.</p>
				<h2>Bootstrap Button Styles</h2>
                <p>Different classes are available in Bootstrap for styling the buttons as well as to indicate the different states or semantic. Button styles can be applied to any element. However, it is applied normally to the <a href="../info_html/ht_a-tag.php"><code>&lt;a&gt;</code></a>, <a href="../info_html/ht_input-tag.php"><code>&lt;input&gt;</code></a>, and <a href="../info_html/ht_button-tag.php"><code>&lt;button&gt;</code></a> elements for the best rendering.</p>
