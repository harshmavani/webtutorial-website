            <h2>Type of Boxes Generated in CSS</h2>
            <p class="space">Every element displayed on a web page generates a rectangular box. The following section describes the types of boxes that may be generated by an element.</p>
            <h2 id="block-level">Block-level Elements and Block Boxes</h2>
            <p>Block-level elements are those elements that are formatted visually as blocks (i.e. takes up the full width available), with a line break before and after the element. For example, paragraph element (<a href="../info-html/html-p-tag.php"><code>&lt;p&gt;</code></a>), headings (<a href="../info-html/html-headings-tag.php"><code>&lt;h1&gt; to &lt;h6&gt;</code></a>), divisions (<a href="../info-html/html-div-tag.php"><code>&lt;div&gt;</code></a>) etc.</p>
            <p>Generally, block-level elements may contain inline elements and other block-level elements.</p>
            <hr />
            <h2 id="inline-level">Inline-level Elements and Inline Boxes</h2>
            <p>Inline-level elements are those elements of the source document that do not form new blocks of content; the content is distributed in lines. For example, emphasized pieces of text within a paragraph (<a href="../info-html/html-em-tag.php"><code>&lt;em&gt;</code></a>), spans (<a href="../info-html/html-span-tag.php"><code>&lt;span&gt;</code></a>), strong element (<a href="../info-html/html-strong-tag.php"><code>&lt;strong&gt;</code></a>) etc.</p>
            <p>Inline elements typically may only contain text and other inline elements.</p>
