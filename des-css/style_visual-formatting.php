            <h1>CSS <span>Visual Formatting</span></h1>
            <p class="summary">The visual formatting model typically explains how elements in the document tree are processed for visual media such as computer screen.</p>
            <h2>CSS Visual Formatting Model</h2>
            <p>The CSS visual formatting model is the algorithm that is used to process the documents for visual media. In the visual formatting model, each element in the document tree generates zero or more boxes according to the <a href="style_box-model.php">box model</a>.</p>
            <p>The layout of these boxes is depends on the following factors:</p>
            <ul>
            	<li>box dimensions.</li>
                <li>type of the element (block or inline).</li>
                <li>positioning scheme (normal flow, <a href="style_float.php">float</a>, and <a href="style_position.php#absolute-positioning">absolute positioning</a>).</li>
                <li>relationships between elements in the <a href="../definitions.php#document-tree">document tree</a>.</li>
                <li>external information e.g. viewport size, built-in dimensions of images, etc.</li>
            </ul>
