            <h1>CSS <span>Validation</span></h1>
            <p class="summary">CSS validation is the process of checking the code against the formal guidelines and standards set by the Wide Web Consortium (W3C) for CSS document.</p>
            <h2>Why Validate Your CSS Code</h2>
            <p>As a beginner, it is very common that you will make mistake in writing your CSS code. Incorrect or non-standard code may cause unexpected results in how your page displayed or functions in a web browser.</p>
            <p class="space">The World Wide Web Consortium (W3C) has created a great tool <a href="https://jigsaw.w3.org/style_validator/" target="_blank" rel="nofollow">https://jigsaw.w3.org/style_validator/</a> to automatically check your style sheets, and point out any problems/errors your code might have, such as invalid CSS property missing closing bracket or missing semicolon (;) etc. It is absolutely free.</p>
            <h2>Validating a Website</h2>
            <p>Website validation is the process to ensure that the pages of a website conform to the formal guidelines and standards set by the World Wide Web Consortium (W3C).</p>
            <p>There are several specific reasons for validating a website, some of them are:</p>
            <ul>
            	<li>It helps to create Web pages that are cross-browser, cross-platform compatible. It also likely to be compatible with the future version of Web browsers and Web standards.</li>
                <li>Standards compliant web pages increase the search engines spider visibility and your pages will more likely be appear in search results.</li>
                <li>It will reduce unexpected errors and make your web pages more accessible to the visitor of your website.</li>
            </ul>
