                <h2>Advantages of Using CSS</h2>
                <p>The biggest advantage of CSS is that it allows the separation of style and layout from the content of the document. Here are some more advantages, why one should start using CSS?</p>
                <ul>
                    <li><strong>CSS Save Lots of Time</strong> &mdash; CSS gives lots of flexibility to set the style properties of an element. You can write CSS once; and then the same code can be applied to the groups of HTML elements, and can also be reused in multiple HTML pages.</li>
                    <li><strong>Easy Maintenance</strong> &mdash; CSS provides an easy means to update the formatting of the documents, and to maintain the consistency across multiple documents. Because the content of the entire set of web pages can be easily controlled using one or more style sheets.</li>
                    <li><strong>Pages Load Faster</strong> &mdash; CSS enables multiple pages to share the formatting information, which reduces complexity and repetition in the structural contents of the documents. It significantly reduces the file transfer size, which results in a faster page loading.</li>
                    <li><strong>Superior Styles to HTML</strong> &mdash; CSS has much wider presentation capabilities than HTML and provide much better control over the layout of your web pages. So you can give far better look to your web pages in comparison to the HTML presentational elements and attributes.</li>
                    <li><strong>Multiple Device Compatibility</strong> &mdash; CSS also allows web pages to be optimized for more than one type of device or media. Using CSS the same HTML document can be presented in different viewing styles for different rendering devices such as desktop, cell phones, etc.</li>
                </ul>
