                <h2>What You Can Do with CSS</h2>
                <p>There are lot more things you can do with CSS.</p>
                <ul>
                    <li>You can easily apply same style rules on multiple elements.</li>                
                    <li>You can control the presentation of multiple pages of a website with a single style sheet.</li>
                    <li>You can present the same page differently on different devices.</li>
                    <li>You can style dynamic states of elements such as hover, focus, etc. that isn't possible otherwise.</li>
                    <li>You can change the position of an element on a web page without changing the markup.</li>                
                    <li>You can alter the display of existing HTML elements.</li>
                    <li>You can transform elements like scale, rotate, skew, etc. in 2D or 3D space.</li>
                    <li>You can create animations and transitions effects without using any JavaScript.</li>
                    <li>You can create print friendly version of your web pages.</li>
                </ul>
                <p>The list does not end here, there are many other interesting things that you can do with CSS. You will learn about all of them in detail in upcoming chapters.</p>
