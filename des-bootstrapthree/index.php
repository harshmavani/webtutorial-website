				<p>If you have had some experience with any front-end framework, you might be wondering what makes Bootstrap so special. Here are some advantages why one should opt for Bootstrap framework:</p>
				<ul>
					<li><strong>Save lots of time</strong> &mdash; You can save lots of time and efforts using the Bootstrap predefined design templates and classes and concentrate on other development&thinsp;work.</li>
					<li><strong>Responsive features</strong> &mdash; Using Bootstrap you can easily create responsive websites that appear more appropriately on different devices and screen resolutions without any change in markup.</li>
					<li><strong>Consistent design</strong> &mdash; All Bootstrap components share the same design templates and styles through a central library, so the design and layout of your web pages will be consistent.</li>
					<li><strong>Easy to use</strong> &mdash; Bootstrap is very easy to use. Anybody with the basic working knowledge of HTML, CSS and JavaScript can start development with Bootstrap.</li>
